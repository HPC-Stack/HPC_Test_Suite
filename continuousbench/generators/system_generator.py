import os
from datetime import datetime

import questionary
import yaml


# ---------------------------------------------------------------------------
# Template pieces – mirroring the paramrudra/settings.py structure exactly
# ---------------------------------------------------------------------------

_DEVICE_BLOCK = """\
                        {{
                            'type': '{dtype}',
                            'arch': '{arch}',
                            'model': '{model}',
                            'num_devices': {num_devices}
                        }},"""

_PROCESSOR_BLOCK = """\
                    'processor': {{
                        'num_cpus': {num_cpus},
                        'num_sockets': {num_sockets},
                        'num_cpus_per_socket': {num_cpus_per_socket},
                    }},"""

_RESOURCES_BLOCK_MEMORY = """\
                    'resources': [
                        {{
                            'name': 'memory',
                            'options': ['--mem={{size}}', '--time=10:00:00'],
                        }}
                    ],"""

_RESOURCES_BLOCK_GPU = """\
                    'resources': [
                        {{
                            'name': 'memory',
                            'options': ['--mem={{size}}'],
                        }}
                    ],"""

_PARTITION_TEMPLATE = """\
                {{
                    'name': '{pname}',
                    'descr': '{pdescr}',{extras_line}{modules_line}{scheduler_line}
                    'launcher': '{launcher}',{access_line}{environs_line}{max_jobs_line}{features_line}{devices_block}{processor_block}{resources_block}
                }},"""

_SYSTEM_TEMPLATE = """\
# config/multifile/{sysname}/settings.py
# System definition for {sysname}
# Generated by continusbench add-system on {generated_at}

import os

site_configuration = {{
    'systems': [
        {{
            'name': '{sysname}',
            'descr': '{description}',
            'hostnames': ['.*'],
            'modules_system': 'spack',
            'resourcesdir': '{resourcesdir}',
            'stagedir': '{stagedir}',
            'outputdir': '{outputdir}',
            'partitions': [
{partitions}
            ],
        }},
    ],
}}
"""


